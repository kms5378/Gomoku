create extension if not exists pgcrypto;

do $$
begin
  if not exists (select 1 from pg_type where typname = 'room_status') then
    create type public.room_status as enum ('waiting', 'playing', 'finished');
  end if;

  if not exists (select 1 from pg_type where typname = 'stone_color') then
    create type public.stone_color as enum ('black', 'white');
  end if;
end $$;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  nickname text not null check (char_length(nickname) between 1 and 24),
  rating integer not null default 0 check (rating >= 0),
  wins integer not null default 0 check (wins >= 0),
  losses integer not null default 0 check (losses >= 0),
  draws integer not null default 0 check (draws >= 0),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.rooms (
  id uuid primary key default gen_random_uuid(),
  code text not null unique check (code ~ '^[A-Z0-9]{6}$'),
  status public.room_status not null default 'waiting',
  black_player uuid not null references public.profiles(id) on delete cascade,
  white_player uuid references public.profiles(id) on delete cascade,
  current_turn public.stone_color not null default 'black',
  winner public.stone_color,
  winning_player uuid references public.profiles(id) on delete set null,
  game_index integer not null default 1 check (game_index > 0),
  restart_black boolean not null default false,
  restart_white boolean not null default false,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  finished_at timestamptz,
  constraint different_players check (white_player is null or black_player <> white_player)
);

create table if not exists public.moves (
  id bigint generated by default as identity primary key,
  room_id uuid not null references public.rooms(id) on delete cascade,
  game_index integer not null check (game_index > 0),
  player_id uuid not null references public.profiles(id) on delete cascade,
  color public.stone_color not null,
  row integer not null check (row between 0 and 14),
  col integer not null check (col between 0 and 14),
  move_number integer not null check (move_number between 1 and 225),
  created_at timestamptz not null default now(),
  unique (room_id, game_index, row, col),
  unique (room_id, game_index, move_number)
);

create table if not exists public.game_results (
  id bigint generated by default as identity primary key,
  room_id uuid not null references public.rooms(id) on delete cascade,
  game_index integer not null check (game_index > 0),
  black_player uuid not null references public.profiles(id) on delete cascade,
  white_player uuid not null references public.profiles(id) on delete cascade,
  winner_player uuid references public.profiles(id) on delete set null,
  outcome text not null check (outcome in ('black_win', 'white_win', 'draw')),
  created_at timestamptz not null default now(),
  unique (room_id, game_index)
);

create index if not exists moves_room_game_index_idx on public.moves (room_id, game_index, move_number);
create index if not exists rooms_code_idx on public.rooms (code);

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists profiles_touch_updated_at on public.profiles;
create trigger profiles_touch_updated_at
before update on public.profiles
for each row execute function public.touch_updated_at();


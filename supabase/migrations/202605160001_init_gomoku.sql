drop policy if exists "profiles are readable by authenticated users" on public.profiles;
create policy "profiles are readable by authenticated users"
on public.profiles for select
to authenticated
using (true);

drop policy if exists "users can update their own profile" on public.profiles;
create policy "users can update their own profile"
on public.profiles for update
to authenticated
using (id = auth.uid())
with check (id = auth.uid());

drop policy if exists "room players can read their rooms" on public.rooms;
create policy "room players can read their rooms"
on public.rooms for select
to authenticated
using (black_player = auth.uid() or white_player = auth.uid());

drop policy if exists "room players can read moves" on public.moves;
create policy "room players can read moves"
on public.moves for select
to authenticated
using (
  exists (
    select 1
    from public.rooms
    where rooms.id = moves.room_id
      and (rooms.black_player = auth.uid() or rooms.white_player = auth.uid())
  )
);

drop policy if exists "room players can read results" on public.game_results;
create policy "room players can read results"
on public.game_results for select
to authenticated
using (black_player = auth.uid() or white_player = auth.uid());

